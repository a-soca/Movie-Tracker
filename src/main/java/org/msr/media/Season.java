package org.msr.media;

public abstract class Season {
    public abstract int getSeasonNumber();
    public abstract int setSeasonNumber(int seasonNumber);
    public abstract int getYearOfRelease();
    public abstract void setYearOfRelease(int yearOfRelease);
    public abstract int getNumberOfEpisodes();
    public abstract void setNumberOfEpisodes(int numberOfEpisodes);
}
